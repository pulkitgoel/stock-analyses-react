// Build-time generator: markdown frontmatter is the SINGLE SOURCE OF TRUTH.
//
// Scans public/analyses/*.md, validates each file's frontmatter, then emits:
//   • src/data/analyses.generated.ts  — typed array imported by the app
//   • public/analyses.json            — { analyses: [...] } for tooling/backend
//
// The build fails loudly if any file is missing a required field, so a
// malformed publish can never ship silently.
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const REPO = path.resolve(__dirname, '..');
const ANALYSES_DIR = path.join(REPO, 'public', 'analyses');
const TS_OUT = path.join(REPO, 'src', 'data', 'analyses.generated.ts');
const JSON_OUT = path.join(REPO, 'public', 'analyses.json');

const { parseFrontmatter } = await import(
  pathToFileURL(path.join(__dirname, 'lib', 'frontmatter.js')).href
);

const REQUIRED_STRING = ['title', 'ticker', 'date', 'summary'];

function validate(slug, data) {
  const errors = [];
  for (const key of REQUIRED_STRING) {
    if (typeof data[key] !== 'string' || !data[key].trim()) {
      errors.push(`"${key}" must be a non-empty string`);
    }
  }
  if (typeof data.date === 'string' && !/^\d{4}-\d{2}-\d{2}$/.test(data.date)) {
    errors.push(`"date" must be ISO format YYYY-MM-DD (got "${data.date}")`);
  }
  if (!Array.isArray(data.tags) || data.tags.some((t) => typeof t !== 'string')) {
    errors.push('"tags" must be an array of strings, e.g. ["ev","auto"]');
  }
  return errors;
}

const files = fs
  .readdirSync(ANALYSES_DIR)
  .filter((f) => f.endsWith('.md'))
  .sort();

const analyses = [];
const problems = [];

for (const file of files) {
  const slug = file.replace(/\.md$/, '');
  const raw = fs.readFileSync(path.join(ANALYSES_DIR, file), 'utf8');
  const { data } = parseFrontmatter(raw);

  const errors = validate(slug, data);
  if (errors.length) {
    problems.push(`  • ${file}:\n      - ${errors.join('\n      - ')}`);
    continue;
  }

  analyses.push({
    title: data.title,
    ticker: data.ticker,
    file,
    slug,
    date: data.date,
    tags: data.tags,
    summary: data.summary,
    ...(data.model ? { model: data.model } : {}),
  });
}

if (problems.length) {
  console.error(
    `\n✗ generate-analyses-data: ${problems.length} file(s) have invalid frontmatter:\n${problems.join('\n')}\n`
  );
  process.exit(1);
}

// Newest first; break ties by slug for deterministic output.
analyses.sort((a, b) => (a.date < b.date ? 1 : a.date > b.date ? -1 : a.slug.localeCompare(b.slug)));

const banner =
  '// AUTO-GENERATED by scripts/generate-analyses-data.js — DO NOT EDIT.\n' +
  '// Source of truth: frontmatter in public/analyses/*.md\n' +
  "// Regenerate with: node scripts/generate-analyses-data.js\n";

const ts =
  banner +
  "import type { Analysis } from '../types/analysis';\n\n" +
  `export const ANALYSES: Analysis[] = ${JSON.stringify(analyses, null, 2)};\n`;

fs.mkdirSync(path.dirname(TS_OUT), { recursive: true });
fs.writeFileSync(TS_OUT, ts, 'utf8');
fs.writeFileSync(JSON_OUT, JSON.stringify({ analyses }, null, 2) + '\n', 'utf8');

console.log(`✓ generate-analyses-data: wrote ${analyses.length} analyses`);
console.log(`    → ${path.relative(REPO, TS_OUT)}`);
console.log(`    → ${path.relative(REPO, JSON_OUT)}`);
